package com.learning.variables;

public class Declarations {
    public static void main(String[] args) {
        String s="heloo";     //store array of char
        int number=10;        // store integers     -4byte
        short short_number=20;  // store integers     -2byte
        double db=10.00;        // store decimal numbers     -8byte
        long lg=20000;             // store integers     -8byte
        float fl=1000;           // store decimal     -4byte
        char ch='s';               // store single character
        boolean status=true;      // store true or false as 1 or 0
        System.out.println(status);
        System.out.println(s);
        System.out.println(number);
        System.out.println(short_number);
        System.out.println(db);
        System.out.println(lg);
        System.out.println(ch);
        System.out.println(fl);


    }
}
